/*
Type Of Arithmetic Operators
1)  = :  x=y
2) -= : x=x-y : x -= y
3) += : x=x+y : x += y
4) *= : x=x*y : x *= y
5) /= : x=x/y : x /= y
6) %= : x=x%y : x %= y
7) **=	x **= y	x = x ** y
*/

